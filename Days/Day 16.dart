void main() {
  // Conditions Expressions
   
  //  If statement
  // Example:
  const salary = 30;

  if(salary < 50) {
    print('You are sacked');
  }

  // Another example
  const salary_ = 30;

  if(salary_ >= 50) {
    print('You have been promoted');
  } else {
    print('Sorry, try again next time!');
  };


  // Declaring with String
  const sentence = 'condition';
  if(sentence == 'condition') {
    print('sentence');
  }
}